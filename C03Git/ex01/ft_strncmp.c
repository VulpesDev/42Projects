int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	ni;

	ni = 0;
	if (n == 0)
		return (0);
	while ((*s1 || *s2) && ni < n)
	{
		ni++;
		s1++;
		s2++;
		if (*(s1 - 1) != *(s2 - 1))
			break ;
	}
	return (*(s1 - 1) - *(s2 - 1));
}
